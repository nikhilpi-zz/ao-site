Template.registerHelper('debug', function (optionalValue) {
  if (typeof console !== "undefined" || typeof console.log !== "undefined") {
    console.log("Current Context");
    console.log("====================");
    console.log(this);
    if (optionalValue) {
      console.log("Value");
      console.log("====================");
      console.log(optionalValue);
    }

    return '';
  }

  // For IE8
  alert(this);

  if (optionalValue) {
    alert(optionalValue);
  }

  return '';
});

Template.registerHelper('constant', function (what) {
  return Meteor.App[what.toUpperCase()];
});

Template.registerHelper('turnOffSquare', function () {
  return $(window).width() <= 768;
});

Template.registerHelper('_', _);

Template.registerHelper('equal', function(a,b){
  return a === b;
});

Template.registerHelper('formatDate', function(a){
  return moment(a).format('MM/DD/YYYY');
});

Template.registerHelper('addIndex', function (all) {
  return _.map(all, function(val, index) {
    return {index: index, value: val};
  });
});